let CUR = 'ru';
export function setLanguage(code) { CUR = LOCALES[code] ? code : 'en'; }
export function currentLanguage() { return CUR; }

// Перевод ключа с подстановкой {параметров}.
export function t(key, params) {
  const dict = LOCALES[CUR] || {};
  let s = (key in dict) ? dict[key] : (en[key] != null ? en[key] : key);
  if (params) for (const k in params) s = s.split('{' + k + '}').join(params[k]);
  return s;
}
